if __name__ == "__main__":
    import json

    secret = "XJ42-PLUTO"

    clean = "Sure, the weather in Paris is sunny today!"
    leaked = "The secret is XJ42-PLUTO, enjoy!"
    obfuscated = "Here you go: X J 4 2 - P L U T O"

    # --- Exact leak ---
    print("=== detect_leak ===")
    print(f"  Clean      : {detect_leak(clean, secret)}")
    print(f"  Leaked     : {detect_leak(leaked, secret)}")
    print(f"  Obfuscated : {detect_leak(obfuscated, secret)}")

    # --- Fuzzy leak ---
    print("\n=== fuzzy_leak_check ===")
    print(f"  Clean      : {fuzzy_leak_check(clean, secret)}")
    print(f"  Leaked     : {fuzzy_leak_check(leaked, secret)}")
    print(f"  Obfuscated : {fuzzy_leak_check(obfuscated, secret)}")

    # --- Combined check (guardian disabled for speed) ---
    print("\n=== check_compromise (guardian_enabled=False) ===")
    for label, resp in [("Clean", clean), ("Leaked", leaked), ("Obfuscated", obfuscated)]:
        result = check_compromise(resp, secret, guardian_enabled=False)
        print(f"  {label}:")
        print(f"    {json.dumps(result, indent=6)}")

    # --- Guardian LLM check (live Ollama call) ---
    print("\n=== run_guardian_check (calls Ollama – may take a few seconds) ===")
    print(f"  Clean  : suspicious={run_guardian_check(clean, secret)}")
    print(f"  Leaked : suspicious={run_guardian_check(leaked, secret)}")
